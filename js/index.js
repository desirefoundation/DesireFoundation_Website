// JS and jQuery functions for index.html

// Load navbar and add on click handler
$(function () {
  $(".nav-container").load("common/navbar.html", () => {
    document.getElementById("menu-btn").addEventListener("click", () => {
      let menuItems = document.querySelectorAll("nav ul li");

      menuItems.forEach((item) => {
        item.classList.toggle("visible");
      });
    });
  });
});

// Load up the footer
$(function () {
  $(".footer-container").load("common/footer.html");
});

// Timeline Animation
$(function () {
  window.sr = ScrollReveal();

  if ($(window).width() < 768) {
    if ($(".timeline-content").hasClass("js--fadeInLeft")) {
      $(".timeline-content")
        .removeClass("js--fadeInLeft")
        .addClass("js--fadeInRight");
    }

    sr.reveal(".js--fadeInRight", {
      origin: "right",
      distance: "300px",
      easing: "ease-in-out",
      duration: 800,
    });
  } else {
    sr.reveal(".js--fadeInLeft", {
      origin: "left",
      distance: "300px",
      easing: "ease-in-out",
      duration: 800,
    });

    sr.reveal(".js--fadeInRight", {
      origin: "right",
      distance: "300px",
      easing: "ease-in-out",
      duration: 800,
    });
  }

  sr.reveal(".js--fadeInLeft", {
    origin: "left",
    distance: "300px",
    easing: "ease-in-out",
    duration: 800,
  });

  sr.reveal(".js--fadeInRight", {
    origin: "right",
    distance: "300px",
    easing: "ease-in-out",
    duration: 800,
  });
});
